package com.Praktikum.Modul_3.codelab;

public class converter {

  protected double setValueToM(double m ){
    return m*0.9144;
  }

  protected double setValueToCM(double cm ){
    return cm*91.44;
  }

  protected double setValueToFeet(double m ){
    return m*3;
  }
  protected double setValueToMiles(double m ){
    return m*0.000568182;
  }

}
